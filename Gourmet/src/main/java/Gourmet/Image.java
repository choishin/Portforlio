package Gourmet;

public class Image {
	private String gourmet_name;
	private String gourmet_image;
	
	public String getGourmet_name() {
		return gourmet_name;
	}
	public void setGourmet_name(String gourmet_name) {
		this.gourmet_name = gourmet_name;
	}
	public String getGourmet_image() {
		return gourmet_image;
	}
	public void setGourmet_image(String gourmet_image) {
		this.gourmet_image = gourmet_image;
	}
	
	

}
