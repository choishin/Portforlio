package Gourmet;

public class Price {
	private String gourmet_name;
	private String gourmet_menu;
	private String gourmet_price;
	
	public String getGourmet_name() {
		return gourmet_name;
	}
	public void setGourmet_name(String gourmet_name) {
		this.gourmet_name = gourmet_name;
	}
	public String getGourmet_menu() {
		return gourmet_menu;
	}
	public void setGourmet_menu(String gourmet_munu) {
		this.gourmet_menu = gourmet_munu;
	}
	public String getGourmet_price() {
		return gourmet_price;
	}
	public void setGourmet_price(String gourmet_price) {
		this.gourmet_price = gourmet_price;
	}
	
	

}
